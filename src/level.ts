export const findTile = (tile: TileType): { col: number; row: number } => {
  for (let r = 0; r < LEVEL.length; r++) {
    for (let c = 0; c < LEVEL[r].length; c++) {
      if (LEVEL[r][c] === tile) return { col: c, row: r };
    }
  }
  throw new Error(`tile ${tile} not found in level`);
};
